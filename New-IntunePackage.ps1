$remediatePath = Join-Path $OutputPath "$Prefix-Remediate.ps1"
$remediateScript | Out-File -FilePath $remediatePath -Encoding UTF8
Write-Host "📄 Remediation script: $remediatePath" -ForegroundColor Yellow

