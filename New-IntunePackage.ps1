$detectScript = $scriptTemplate `
    -replace '\{MODE\}', 'Detection' `
    -replace '\{MODEVALUE\}', 'Detect' `
    -replace '\{VERBOSELOGGING\}', $(if ($VerboseLogging) { '$true' } else { '$false' }) `
